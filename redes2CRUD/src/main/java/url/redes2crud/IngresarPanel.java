    private void txtIngresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtIngresarMouseClicked
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            PreparedStatement pst = cn.prepareStatement("CALL IngresarProducto (?,?,?,?)");
            pst.setString(1,(txtname.getText()));
            pst.setDouble(2, Integer.parseInt(txtprecio.getText()));
            pst.setString(3, txtdes.getText());
            pst.setInt(4, Integer.parseInt(txttotal.getText()));
            pst.executeUpdate();
            //JOptionPane.showMessageDialog(rootPane, "Ingresado");
            jLabel5.setText("Producto ingresada");
            txtname.setText("");
            txtprecio.setText("");
            txtdes.setText("");
            txttotal.setText("");

        } catch (SQLException ex) {
            Logger.getLogger(IngresarPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtIngresarMouseClicked

